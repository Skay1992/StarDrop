def split_document(
    document: str,
    max_length: int = MAX_DOCUMENT_PAGE_LENGTH,
) -> List[str]:
    paragraphs = document.split("\n\n")
    pages: List[str] = []
    current: List[str] = []

    for paragraph in paragraphs:
        if len(paragraph) > max_length:
            raise ValueError("Абзац юридического документа превышает лимит страницы")

        candidate = "\n\n".join([*current, paragraph])
        if current and len(candidate) > max_length:
            pages.append("\n\n".join(current))
            current = [paragraph]
        else:
            current.append(paragraph)

    if current:
        pages.append("\n\n".join(current))

    return pages
